    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(connected == 0){
            if(jTextField1.getText().isEmpty())
                JOptionPane.showMessageDialog(null,"Broker TCP/IP address is missing","Information missing",JOptionPane.WARNING_MESSAGE);
            else{
                mqtt.connect(jTextField1.getText());
                jButton1.setText("Disconnect");
                jTextField2.setEnabled(true);
                jButton2.setEnabled(true);
                jTextField3.setEnabled(true);
                jButton3.setEnabled(true);
                jTextArea1.setEnabled(true);
                connected = 1;
            }
        }else{
            mqtt.disconnect();
            jButton1.setText("Connect");
            jButton2.setText("Subscribe");
            jTextField2.setEnabled(false);
            jButton2.setEnabled(false);
            jTextField3.setEnabled(false);
            jButton3.setEnabled(false);
            jTextArea1.setEnabled(false);
            jTextField2.setText("");
            jTextField3.setText("");
            jTextArea1.setText("");
            subscribed = 0;
            connected = 0;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(subscribed == 0){
            if(jTextField2.getText().isEmpty())
                JOptionPane.showMessageDialog(null,"Topic is missing","Information missing",JOptionPane.WARNING_MESSAGE);
            else{
                mqtt.subscribe(jTextField2.getText());
                jButton2.setText("Unsubscribe");
                subscribed = 1;
            }
        }else{
            mqtt.unsubscribe();
            jButton2.setText("Subscribe");
            subscribed = 0;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextField3.getText().isEmpty() || jTextArea1.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Topic or message is missing","Information missing",JOptionPane.WARNING_MESSAGE);
        else{
            mqtt.publish(jTextField3.getText(), jTextArea1.getText());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

